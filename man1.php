Manchester United Football Club is a professional football club based in the Old Trafford area of Manchester, England. <br>
The club competes in the Premier League, the top division in the English football league system. Nicknamed the Red Devils, <br>
it was founded as Newton Heath LYR Football Club in 1878, but changed its name to Manchester United in 1902. <br>
The club moved from Newton Heath to its current stadium, Old Trafford, in 1910.
<br><br>
Manchester United is one of the most successful clubs in English football,[5][6] having won a record 20 League titles, <br>
12 FA Cups, five League Cups, and a record 21 FA Community Shields. They have won the European Cup/UEFA Champions League three times, <br>
and the UEFA Europa League, the UEFA Cup Winners' Cup, the UEFA Super Cup, the Intercontinental Cup and the FIFA Club World Cup once each. <br>
In 1968, under the management of Matt Busby, 10 years after eight of the club's players were killed in the Munich air disaster, <br>
they became the first English club to win the European Cup. Alex Ferguson is the club's longest-serving and most successful manager, <br>
winning 38 trophies, including 13 league titles, 5 FA Cups, and 2 UEFA Champions League titles between 1986 and 2013.[7][8][9] In the 1998–99 season, <br>
under Ferguson, the club became the first in the history of English football to achieve the European treble of the Premier League, FA Cup, and UEFA Champions League. <br>
[10] In winning the UEFA Europa League under José Mourinho in 2016–17, the most recent trophy won by the club, <br>
they also became one of five clubs to have won the original three main UEFA club competitions (the Champions League, Europa League and Cup Winners' Cup).
<br><br>
Manchester United is one of the most widely supported football clubs in the world[11][12] and has rivalries with Liverpool, Manchester City, Arsenal and Leeds United. <br>
Manchester United was the highest-earning football club in the world for 2016–17, with an annual revenue of €676.3 million,[13] and <br>
the world's third most valuable football club in 2019, valued at £3.15 billion ($3.81 billion).[14] After being floated on the London Stock Exchange in 1991, <br>
the club was taken private in 2005 after a purchase by American businessman Malcolm Glazer valued at almost £800 million, of which over £500 million of borrowed money <br>
became the club's debt.[15] From 2012, some shares of the club were listed on the New York Stock Exchange, although the Glazer family retains overall ownership and control of the club. <br> 
